    private void JbtnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnEqualActionPerformed
//        firstValue = "0";
        double d1 = Double.parseDouble(firstValue);
        double d2 = Double.parseDouble(txtValueField.getText());
        double total = 0.0;
        if (sign.equals("+")) {
            total = d1 + d2;
        } else if (sign.equals("-")) {
            total = d1 - d2;
        } else if (sign.equals("*")) {
            total = d1 * d2;
        } else if (sign.equals("/")) {
            total = d1 / d2;
        } else {
            JOptionPane.showMessageDialog(null, "Operater is not defined");
        }
        txtValueField.setText(total + "");
        sign = "";
        firstValue = "0";
    }//GEN-LAST:event_JbtnEqualActionPerformed

