    private void JbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnUpdateActionPerformed
        try {
            txtId.enableInputMethods(false);

            int rownumber = JtblSearch.getSelectedRow();
            txtId.setText(JtblSearch.getValueAt(rownumber, 0) + "");

            String SCurrentStock = (String) JtblSearch.getValueAt(rownumber, 5);
            int CurrentStock = Integer.parseInt(SCurrentStock);

            int UpdateStock = Integer.parseInt(txtStock.getText());
            int NewStock = CurrentStock + UpdateStock;

            String up = "Update `accessories` set Name = '" + txtName.getText() + "',"
                    + "Brand = '" + txtBrand.getText() + "',Type = '" + txtType.getText() + "',"
                    + "`Unit Price` = '" + txtPrice.getText() + "',Stock = '" + NewStock + "' "
                    + "where id='" + txtId.getText() + "'";
            MyConn.save(up);
            searchIN_UP_DEL();
            UP_toGRN();
            JOptionPane.showMessageDialog(this, "The item successfully updated");

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_JbtnUpdateActionPerformed

    private void JbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnDeleteActionPerformed
        try {
            txtId.enableInputMethods(false);

            int rownumber = JtblSearch.getSelectedRow();
            int stock = Integer.parseInt(txtStock.getText());
            stock = stock - 2 * stock;

            String delete = "Delete from accessories where ID = '"
                    + (JtblSearch.getValueAt(rownumber, 0) + "") + "'";
            MyConn.save(delete);

            searchIN_UP_DEL();

            try {
                String sqlINUP = "INSERT into grn (`DB Table`, `Item ID`, `Added Stock`,"
                        + "`Other changes_Name`, `Other changese_Brand_Author`,"
                        + "`Other changes_Type`, `Other changes_Unit Price`, `Date and Time`)"
                        + "values('Accessories','" + txtId.getText() + "','" + stock + "',"
                        + "'" + txtName.getText() + "','" + txtBrand.getText() + "',"
                        + "'" + txtType.getText() + "','" + txtPrice.getText() + "',current_timestamp)";
                MyConn.save(sqlINUP);
            } catch (Exception e) {
                e.printStackTrace();
            }

            JOptionPane.showMessageDialog(this, "The item successfully deleted");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_JbtnDeleteActionPerformed

